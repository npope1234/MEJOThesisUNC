function renderInlineLinks(text) {
  const escaped = escapeHTML(text);

  return escaped.replace(
    /\[([^\]]+)\]\((https?:\/\/[^\s)]+)\)/g,
    '<a class="story-link" href="$2" target="_blank" rel="noopener noreferrer">$1</a>'
  );
}

function renderStoryBody(body = []) {
  return body.map((item) => {
    if (typeof item === 'string') {
      return `<p>${renderInlineLinks(item)}</p>`;
    }

    if (item.type === 'subhead') {
      return `<h3 class="story-subhead">${escapeHTML(item.text)}</h3>`;
    }

    if (item.type === 'image') {
      return `
        <figure class="inline-story-image">
          <img src="${escapeHTML(item.src)}" alt="${escapeHTML(item.alt || '')}">
          ${item.caption ? `<figcaption>${renderInlineLinks(item.caption)}</figcaption>` : ''}
        </figure>
      `;
    }

    return '';
  }).join('');
}

function selectStory(index, updateHash = false) {
  const story = stories[index];

  if (updateHash) {
    history.replaceState(null, '', `#${getStorySlug(story)}`);
  }

  storyDetail.scrollTop = 0;

  storyList.querySelectorAll('.story-card').forEach((card) => {
    card.classList.toggle('active', Number(card.dataset.index) === index);
  });

  const storyParagraphs = renderStoryBody(story.storyBody);

  storyDetail.innerHTML = `
    <div class="eyebrow">Selected interview story</div>
    <h2>${escapeHTML(story.name)}</h2>

    <p class="subtle">
      <strong>${escapeHTML(story.role)}</strong><br>
      ${escapeHTML(story.affiliation)}
    </p>

    ${story.image ? `
      <figure class="profile-image-block">
        <div class="story-detail-visual story-detail-visual-image">
          <img src="${escapeHTML(story.image)}" alt="Portrait of ${escapeHTML(story.name)}">
        </div>
        ${story.caption ? `<figcaption class="profile-image-caption">${escapeHTML(story.caption)}</figcaption>` : ''}
      </figure>
    ` : ''}

    <p class="quote">${escapeHTML(story.dek)}</p>

    <div class="info-block">
      <h3>${escapeHTML(story.title)}</h3>
      ${storyParagraphs}
    </div>
  `;
}

