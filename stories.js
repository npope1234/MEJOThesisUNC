function escapeHTML(value) {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

const storySlugs = {
  'Dr. Alan Weakley': 'alan-weakley',
  'Dr. Charles “Chuck” Bargeron': 'chuck-bargeron',
  'Ming Shen': 'ming-shen',
  'Dr. Danielle Hiraldo': 'danielle-hiraldo',
  'Philip Bell': 'philip-bell',
  'Larry Edwards': 'larry-edwards'
};

function getStorySlug(story) {
  return storySlugs[story.name] || story.name
    .toLowerCase()
    .replace(/^dr\.\s*/, '')
    .replace(/[“”"'.]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

